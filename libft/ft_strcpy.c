#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t i;

	i = 0;
	while (1)
	{
		dst[i] = src[i];
		if (dst[i] == '\0')
		{
			return (dst);
		}
		i++;
	}
}
